var buttons = document.querySelectorAll('.button4');

buttons.forEach(function(button) {
  var isClicked = false; // เริ่มต้นด้วยสถานะ "ยังไม่ถูกคลิก"
  
  button.addEventListener('click', function() {
    if (!isClicked) {
      button.style.backgroundColor = '#4CAF50'; /* สีเขียว */
      isClicked = true;
    } else {
      button.style.backgroundColor = '#575757'; /* สีเริ่มต้น (สีน้ำเงิน) */
      isClicked = false;
    }
  });
});

